package main.java.org.activity17;

public interface CrocodileStrategy {
	/**
	 * This method makes crocodile to start traversing the board.
	 * 
	 * @param board
	 *            on which crocodile will run.
	 */
	public void traverseBoard(int[][] board);

	/**
	 * This method returns move count, which was needed to exit current
	 * crocodile.
	 * 
	 * @return movecount which was needed to exit through maze.
	 */
	public int getMoveCount();
}
